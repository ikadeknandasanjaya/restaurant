public class Menu {
    String namaMenu;
    double hargaMenu;

    public Menu(String namaMenu, double hargaMenu) {
        this.namaMenu = namaMenu;
        this.hargaMenu = hargaMenu;
    }

    public String getNamaMenu() {
        return namaMenu;
    }

    public double getHargaMenu() {
        return hargaMenu;
    }
}
